package org.asciiart;

import org.fusesource.jansi.Ansi;
import org.fusesource.jansi.AnsiConsole;

import static org.fusesource.jansi.Ansi.*;
import static org.fusesource.jansi.Ansi.Color.*;

public class App 
{
    private static void printAsciiArt(){
        Ansi asciiArt = ansi().eraseScreen()
                .fg(RED).a("__/\\\\\\_____________________/\\\\\\\\\\________________________________________________________        \r\n")
                .fg(RED).a(" _\\/\\\\\\___________________/\\\\\\///_________________________________________________________       \r\n")
                .fg(YELLOW).a("  _\\/\\\\\\_________/\\\\\\_____/\\\\\\___________________________________________________/\\\\\\______      \r\n")
                .fg(YELLOW).a("   _\\/\\\\\\________\\///___/\\\\\\\\\\\\\\\\\\____/\\\\/\\\\\\\\\\\\\\______/\\\\\\\\\\_____/\\\\\\\\\\\\\\\\\\\\__/\\\\\\\\\\\\\\\\\\\\\\_     \r\n")
                .fg(GREEN).a("    _\\/\\\\\\\\\\\\\\\\\\___/\\\\\\_\\////\\\\\\//____\\/\\\\\\/////\\\\\\___/\\\\\\///\\\\\\__\\/\\\\\\//////__\\////\\\\\\////__    \r\n")
                .fg(GREEN).a("     _\\/\\\\\\////\\\\\\_\\/\\\\\\____\\/\\\\\\______\\/\\\\\\___\\///___/\\\\\\__\\//\\\\\\_\\/\\\\\\\\\\\\\\\\\\\\____\\/\\\\\\______   \r\n")
                .fg(CYAN).a("      _\\/\\\\\\__\\/\\\\\\_\\/\\\\\\____\\/\\\\\\______\\/\\\\\\_________\\//\\\\\\__/\\\\\\__\\////////\\\\\\____\\/\\\\\\_/\\\\__  \r\n")
                .fg(CYAN).a("       _\\/\\\\\\\\\\\\\\\\\\__\\/\\\\\\____\\/\\\\\\______\\/\\\\\\__________\\///\\\\\\\\\\/____/\\\\\\\\\\\\\\\\\\\\____\\//\\\\\\\\\\___ \r\n")
                .fg(MAGENTA).a("        _\\/////////___\\///_____\\///_______\\///_____________\\/////_____\\//////////______\\/////____\r\n")
                .fg(MAGENTA).a("         __________________________________________________________________________________________")
                .reset();

        AnsiConsole.systemInstall();
        System.out.println(asciiArt);
        AnsiConsole.systemInstall();
    }

    public static void main( String[] args )
    {
        printAsciiArt();
        System.out.println("Hello, World!");
    }
}

